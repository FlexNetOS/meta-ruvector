//! **M3 — the defensibility gate.** Run the agentic clock against the *fair*
//! windowed baseline (and the constant-rate strawmen) on **real recorded agent
//! traces**, with **pre-registered** thresholds and an **honestly defined**
//! event-to-predict.
//!
//! ```bash
//! cargo run -p emergent-time --example real_trace_eval
//! ```
//!
//! ## Why this example exists
//!
//! M1 made the crate honest: on the *synthetic* failing-workflow trace the
//! agentic clock does **not** beat the fair `WindowedDeltaClock` baseline (the
//! belief-shift windowed detector fires at least as early). The ADR's
//! "Baseline dominance" invariant (§4) is therefore an **unmet gate**, and the
//! only legitimate way to meet (or fail) it is a **real trace vs the fair
//! baseline**. This is that test.
//!
//! ## THE HARD RULE (do not manufacture a win)
//!
//! This whole effort replaces a circular benchmark. If the agentic clock does
//! **not** beat the fair baseline on real data, that is the reported finding and
//! the crate stays honest. A truthful null/mixed result is the success
//! condition here; a manufactured win re-commits the original sin.
//!
//! Everything below the "PRE-REGISTRATION" banner is fixed *before* any lead is
//! computed and is not tuned to the outcome.
//!
//! ## Data source
//!
//! The **Claude Code session transcripts** for this repo:
//! `~/.claude/projects/C--Users-ruv-ruvector/*.jsonl`. These are real agent runs
//! — tool-use sequences, retries, errors, plan changes recorded by Claude Code
//! itself, not generated by this crate.
//!
//! We deliberately do **not** use `.ruvector/intelligence.json`: inspection shows
//! its 51 "trajectories" are flat single-event *success* records (every one has
//! `outcome = completed`, reward ∈ [0.8, 1.0], no multi-step structure and no
//! failure events). It cannot faithfully expose the agentic channels nor define
//! an event-to-predict, so using it would be dishonest. The session JSONL is
//! noisier but real and rich; we map it faithfully and document every proxy.
//!
//! ## Channel mapping (real signal → agentic channel; why each is a fair proxy)
//!
//! For each *step* (one assistant turn that issues ≥1 tool call, with its
//! results), we observe:
//!
//! | Agentic channel | Real signal extracted from the transcript | Why it is a fair, non-circular proxy |
//! |---|---|---|
//! | `belief`  (ΔB) | TF vector over the **tool *types*** invoked this step (Read/Edit/Write/Bash/…) | The agent's working hypothesis about *what kind of work this is*; a shift from reading→editing→running is a genuine belief move, not the planted target. |
//! | `memory`  (ΔM) | cumulative count of **distinct files touched** (Read/Edit/Write paths) | Working-memory growth; monotone context accumulation. |
//! | `retrieval` (ΔR) | volume of **Read / Grep / search** activity this step | Information arriving from outside; the retrieval channel by definition. |
//! | `goal_graph` (ΔG) | a **new user prompt** since the last step (open-goal mass) | A new human instruction reopens the goal graph — a real goal-graph movement. |
//! | `contradiction` (ΔE) | the **`is_error` rate** of this step's tool results | The genuine error signal recorded by the harness. **This is also what defines the event-to-predict**, so it is *excluded* from the honest agentic variant (see below). |
//! | `plan`    (ΔP) | step's **assistant text/thinking length** + **same-tool repetition** | Plan articulation + plan thrash (repeating the same tool = re-trying the same plan branch). |
//!
//! ## The event to predict (defined INDEPENDENTLY of the agentic channels)
//!
//! A real **error cascade**: the first step at/after which the transcript shows a
//! *cluster* of harness `is_error` tool-results (≥ 2 errors inside a short span).
//! The errors come straight from Claude Code's `is_error` flag — **not** from any
//! belief/plan/contradiction delta the agentic clock computes. So predicting it is
//! a genuine forecast, not a tautology.
//!
//! ## The circularity guard (the honest variant)
//!
//! Because the `contradiction` channel is derived from the **same** `is_error`
//! flag that defines the event, letting the agentic clock *see* contradiction
//! would let it "predict" the event by reading the answer. We therefore evaluate
//! **two** agentic variants and report both:
//!
//! * `agentic-full` — all six channels (contradiction included). Diagnostic only;
//!   it can trivially fire *on* the error and is **not** the gate.
//! * `agentic-honest` — contradiction weight set to **0**. The clock must predict
//!   the error cascade from belief/memory/retrieval/goal/plan movement *alone*,
//!   with no sight of the error signal. **This is the real M3 test.**
//!
//! If `agentic-honest` cannot beat the fair baseline, the honest conclusion is
//! that the primitive's value is diagnostic/explainable, not a raw lead win — and
//! we say exactly that.

use std::fs;
use std::path::{Path, PathBuf};

use emergent_time::adaptive::{adaptive_alarm_step, adaptive_early_warning_lead, PageHinkley};
use emergent_time::agentic_time::{
    alarm_step, early_warning_lead, AgentClock, AgentState, AgentWallClock, AgenticTime,
    AgenticWeights, StepCountClock, TokenCountClock, WindowedDeltaClock,
};

/// Summary statistics of a clock's increments BEFORE the event, plus the alarm
/// threshold its own early baseline implies. This exposes *why* an honest clock
/// may never fire: if the agent churns hard early, the `mean + k·σ` bar is set
/// high and later (genuine) movement never clears it — a real property of real
/// traces, distinct from a degenerate flat signal.
struct ClockDiag {
    inc_max: f64,
    inc_mean: f64,
    baseline_threshold: f64,
    max_after_baseline_before_event: f64,
}

fn diagnose(
    clock: &dyn AgentClock,
    states: &[AgentState],
    event: usize,
    bw: usize,
    k: f64,
) -> ClockDiag {
    let inc = clock.increments(states);
    let n = inc.len();
    let inc_max = inc.iter().cloned().fold(0.0_f64, f64::max);
    let inc_mean = if n > 1 {
        inc[1..].iter().sum::<f64>() / (n as f64 - 1.0)
    } else {
        0.0
    };
    // Same baseline the alarm uses: increments [1..=bw].
    let (threshold, max_after) = if n > bw + 1 {
        let base = &inc[1..=bw];
        let mean = base.iter().sum::<f64>() / base.len() as f64;
        let var = base.iter().map(|x| (x - mean).powi(2)).sum::<f64>() / base.len() as f64;
        let threshold = mean + k * var.sqrt();
        let end = event.min(n);
        let max_after = if end > bw + 1 {
            inc[(bw + 1)..end].iter().cloned().fold(0.0_f64, f64::max)
        } else {
            0.0
        };
        (threshold, max_after)
    } else {
        (f64::INFINITY, 0.0)
    };
    ClockDiag {
        inc_max,
        inc_mean,
        baseline_threshold: threshold,
        max_after_baseline_before_event: max_after,
    }
}

// ===========================================================================
//  ░░░  PRE-REGISTRATION  ░░░
//  These constants are fixed BEFORE any lead is computed and are NOT tuned to
//  the outcome. Changing them to chase a win would void the protocol.
// ===========================================================================

/// Trailing window for both the fair baseline's z-score AND the alarm baseline.
const BASELINE_WINDOW: usize = 10;
/// Alarm sensitivity: fire when a clock's increment exceeds `mean + K_SIGMA·σ`
/// of its own trailing baseline. The same `k` the M1 synthetic benchmark uses.
const K_SIGMA: f64 = 3.0;
/// Error-cascade definition: ≥ `CASCADE_MIN_ERRORS` `is_error` results inside a
/// span of `CASCADE_SPAN` steps marks the event-to-predict. Fixed up front.
const CASCADE_MIN_ERRORS: usize = 2;
const CASCADE_SPAN: usize = 4;
/// A trace must have at least this many steps for an alarm to be meaningful
/// (needs a baseline window + room for the event to follow it).
const MIN_STEPS: usize = BASELINE_WINDOW + 6;

// --- M3b: the ADAPTIVE detector (Page–Hinkley) -----------------------------
// M3 got an honest null with the fixed-window mean+kσ alarm and DIAGNOSED the
// cause: the agent's early-exploration churn poisons a *frozen* baseline, so
// later genuine movement can't clear it. M3 proposed the fix — an ADAPTIVE
// (running-reference) detector. M3b implements that exact fix as a Page–Hinkley
// test (Page 1954; Hinkley 1970) and applies it EQUALLY to the agentic clock
// AND the fair baseline. Its δ, λ are PRE-REGISTERED here, before any adaptive
// lead is computed, and are NOT tuned to the outcome. THE HARD RULE STANDS: the
// same detector runs on both sides; a win is only a win if it is fair.
//
// δ (tolerance): set to a small fraction of a typical honest-agentic increment
//   (mean per-step increment on real traces ≈ 1.5; we tolerate ~10% as "normal"
//   jitter). λ (threshold): a few honest-increment-units of *sustained* drift.
//   Both fixed up front to round, defensible values; not swept against leads.
const PH_DELTA: f64 = 0.15;
const PH_LAMBDA: f64 = 5.0;

/// The pre-registered adaptive detector (upward form: the agentic-drift event is
/// a *rise* in structural movement).
fn adaptive_detector() -> PageHinkley {
    PageHinkley::upward(PH_DELTA, PH_LAMBDA)
}

// ===========================================================================
//  Real-trace adapter: parse Claude Code session JSONL → per-step AgentState.
