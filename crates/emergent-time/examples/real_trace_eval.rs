/// One observed step (an assistant turn that issued ≥1 tool call, plus the
/// results that came back) reduced to the raw, *documented* real signals.
#[derive(Default, Clone)]
struct RawStep {
    /// Tool names invoked this step (e.g. "Read", "Edit", "PowerShell").
    tools: Vec<String>,
    /// Distinct file paths touched this step (Read/Edit/Write).
    files: Vec<String>,
    /// Read/Grep/search tool invocations this step (retrieval volume).
    retrieval_calls: usize,
    /// `is_error` tool-results observed this step (the genuine error signal).
    errors: usize,
    /// A new user prompt arrived just before this step (goal-graph movement).
    new_user_prompt: bool,
    /// Assistant text + thinking length this step (plan articulation, chars).
    text_len: usize,
    /// Cumulative tokens (proxied by cumulative transcript bytes) for the
    /// token-count clock and the fair token-delta baseline.
    cumulative_tokens: u64,
}

/// A parsed real trace: the ordered raw steps and the honestly-derived event.
struct RealTrace {
    name: String,
    steps: Vec<RawStep>,
    /// First step index of the error cascade (the event-to-predict), if any.
    event_index: Option<usize>,
    /// Total `is_error` results in the whole transcript (for reporting).
    total_errors: usize,
}

/// Minimal, dependency-free JSON string scan: count occurrences of a quoted key
/// followed by a given quoted value, and pull simple fields. We avoid a JSON
/// crate (the crate core is dependency-free) and parse line-by-line; each line
/// is one transcript event. We only need a few robust signals, extracted with
/// targeted substring logic rather than a full parser.
fn parse_session(path: &Path) -> Option<RealTrace> {
    let content = fs::read_to_string(path).ok()?;
    let name = path
        .file_stem()
        .and_then(|s| s.to_str())
        .unwrap_or("session")
        .chars()
        .take(8)
        .collect::<String>();

    let mut steps: Vec<RawStep> = Vec::new();
    let mut cumulative_bytes: u64 = 0;
    // Did we see a fresh user prompt since the last tool-issuing step?
    let mut pending_user_prompt = false;

    for line in content.lines() {
        if line.is_empty() {
            continue;
        }
        cumulative_bytes += line.len() as u64;

        // Classify the line by its top-level "type". Real transcript events are
        // one JSON object per line. We use cheap substring tests; they are
        // robust because the harness emits stable key spellings.
        let is_user = line.contains("\"type\":\"user\"");
        let is_assistant = line.contains("\"type\":\"assistant\"");

        // A genuine user *prompt* (not a tool_result-bearing user turn and not a
        // meta/command line): user line that carries no tool_result and is not a
        // slash-command echo. Heuristic but documented.
        if is_user
            && !line.contains("\"tool_result\"")
            && !line.contains("\"isMeta\":true")
            && !line.contains("<command-name>")
            && !line.contains("<local-command-stdout>")
        {
            pending_user_prompt = true;
        }

        if !is_assistant {
            // User tool_result lines carry the error signal for the *current*
            // (most recent) step.
            if is_user && line.contains("\"tool_result\"") {
                if let Some(step) = steps.last_mut() {
                    step.errors += count_occurrences(line, "\"is_error\":true");
                }
            }
            continue;
        }

        // Assistant line. Does it issue any tool calls?
        let tool_uses = extract_tool_names(line);
        if tool_uses.is_empty() {
            // Pure reasoning/answer turn — not a tool-issuing step. We still let
            // its prose contribute to the *next* step's plan signal implicitly by
            // ignoring it here (keeps steps = tool-issuing turns, the natural
            // action cadence).
            continue;
        }

        let mut step = RawStep {
            cumulative_tokens: cumulative_bytes / 4, // ~4 bytes/token, real proxy
            new_user_prompt: pending_user_prompt,
            text_len: line.len(),
            ..Default::default()
        };
        pending_user_prompt = false;

        for t in tool_uses {
            if matches!(
                t.as_str(),
                "Read" | "Grep" | "Glob" | "WebSearch" | "WebFetch"
            ) {
                step.retrieval_calls += 1;
            }
            if matches!(t.as_str(), "Read" | "Edit" | "Write" | "NotebookEdit") {
                if let Some(p) = extract_file_path(line) {
                    if !step.files.contains(&p) {
                        step.files.push(p);
                    }
                }
            }
            step.tools.push(t);
        }
        steps.push(step);
    }

    if steps.len() < MIN_STEPS {
        return None;
    }

    let total_errors: usize = steps.iter().map(|s| s.errors).sum();
    let event_index = find_error_cascade(&steps);

    Some(RealTrace {
        name,
        steps,
        event_index,
        total_errors,
    })
}

/// First step index `i` such that the window `[i, i+CASCADE_SPAN)` contains at
/// least `CASCADE_MIN_ERRORS` error results — the honestly defined event.
fn find_error_cascade(steps: &[RawStep]) -> Option<usize> {
    for i in 0..steps.len() {
        let end = (i + CASCADE_SPAN).min(steps.len());
        let errs: usize = steps[i..end].iter().map(|s| s.errors).sum();
        if errs >= CASCADE_MIN_ERRORS && steps[i].errors >= 1 {
            return Some(i);
        }
    }
    None
}

fn count_occurrences(haystack: &str, needle: &str) -> usize {
    if needle.is_empty() {
        return 0;
    }
    let mut n = 0;
    let mut start = 0;
    while let Some(pos) = haystack[start..].find(needle) {
        n += 1;
        start += pos + needle.len();
    }
    n
}

/// Extract the `name` of every `tool_use` block on an assistant line.
fn extract_tool_names(line: &str) -> Vec<String> {
    let marker = "\"type\":\"tool_use\"";
    let name_key = "\"name\":\"";
    // Tool name and the `tool_use` marker live in the same small object; real
    // transcripts emit them in EITHER order — historically
    // `{"type":"tool_use",…,"name":"Bash"}` but current Claude-Code transcripts
    // emit `{"name":"Bash","type":"tool_use",…}` (name BEFORE type). Searching
    // only *after* the marker silently misses the name-before-type spelling and
    // drops the whole step. So we pair each marker to the *nearest* name within a
    // bounded window, in either direction.
    const WINDOW: usize = 160;

    // All `"name":"VALUE"` occurrences with the byte offset of the marker key.
    let mut name_positions: Vec<(usize, String)> = Vec::new();
    let mut s = 0;
    while let Some(p) = line[s..].find(name_key) {
        let abs = s + p;
        let vstart = abs + name_key.len();
        if let Some(vend) = line[vstart..].find('"') {
            name_positions.push((abs, line[vstart..vstart + vend].to_string()));
            s = vstart + vend + 1;
        } else {
            break;
        }
    }

    let mut names = Vec::new();
    let mut claimed = vec![false; name_positions.len()];
    let mut start = 0;
    while let Some(pos) = line[start..].find(marker) {
        let abs = start + pos;
        // Closest unclaimed name within WINDOW of this marker (either side).
        let mut best: Option<usize> = None;
        let mut best_dist = usize::MAX;
        for (i, (npos, _)) in name_positions.iter().enumerate() {
            if claimed[i] {
                continue;
            }
            let dist = if *npos > abs { npos - abs } else { abs - npos };
            if dist <= WINDOW && dist < best_dist {
                best_dist = dist;
                best = Some(i);
            }
        }
        if let Some(i) = best {
            claimed[i] = true;
            names.push(name_positions[i].1.clone());
        }
        start = abs + marker.len();
    }
    names
}

/// Pull the first `file_path` value on a line (best-effort; one per step is
/// enough for the distinct-files memory signal).
fn extract_file_path(line: &str) -> Option<String> {
    let key = "\"file_path\":\"";
    let pos = line.find(key)?;
    let start = pos + key.len();
    let rest = &line[start..];
    let end = rest.find('"')?;
    Some(rest[..end].to_string())
}

// ===========================================================================
//  Map raw steps → AgentState channel vectors (the documented proxy mapping).
// ===========================================================================

/// Stable ordering of the tool vocabulary so the belief TF-vector dimensions are
/// consistent across steps and traces.
const TOOL_VOCAB: &[&str] = &[
    "Read",
    "Edit",
    "Write",
    "PowerShell",
    "Bash",
    "Grep",
    "Glob",
    "Agent",
    "Task",
    "TodoWrite",
    "WebFetch",
    "WebSearch",
    "NotebookEdit",
];

fn tool_index(name: &str) -> Option<usize> {
    TOOL_VOCAB.iter().position(|&t| t == name)
}

/// Build the per-step `AgentState` series from the raw steps using ONLY the
/// documented proxy mapping above.
fn to_agent_states(steps: &[RawStep]) -> Vec<AgentState> {
    let dim = TOOL_VOCAB.len();
    let mut out = Vec::with_capacity(steps.len());
    let mut cumulative_files: Vec<String> = Vec::new();
    let mut prev_tools: Vec<String> = Vec::new();

    for step in steps {
        // belief: normalized TF vector over tool types this step.
        let mut belief = vec![0.0; dim];
        for t in &step.tools {
            if let Some(i) = tool_index(t) {
                belief[i] += 1.0;
            }
        }
        let norm: f64 = belief.iter().sum();
        if norm > 0.0 {
            for b in &mut belief {
                *b /= norm;
            }
        }

        // memory: cumulative distinct files touched (scalar, embedded as a
        // 1-vector so the L2 distance picks up monotone growth).
        for f in &step.files {
            if !cumulative_files.contains(f) {
                cumulative_files.push(f.clone());
            }
        }
        let memory = vec![cumulative_files.len() as f64];

        // retrieval: retrieval-call volume this step (1-vector).
        let retrieval = vec![step.retrieval_calls as f64];

        // goal_graph: open-goal mass — bumps when a new user prompt arrived.
        let goal_graph = if step.new_user_prompt { 1.0 } else { 0.0 };

        // contradiction: this step's error rate, clamped to [0,1].
        let contradiction = (step.errors as f64 / (step.tools.len().max(1) as f64)).min(1.0);

        // plan: [normalized text length, same-tool-repetition score]. Repetition
        // = fraction of this step's tools that also appeared last step (plan
        // thrash: re-trying the same plan branch).
        let repeat = if prev_tools.is_empty() || step.tools.is_empty() {
            0.0
        } else {
            let shared = step.tools.iter().filter(|t| prev_tools.contains(t)).count();
            shared as f64 / step.tools.len() as f64
        };
        let plan = vec![(step.text_len as f64).ln_1p() / 10.0, repeat];
        prev_tools = step.tools.clone();

        out.push(AgentState {
            belief,
            memory,
            retrieval,
            goal_graph,
            contradiction,
            plan,
            tokens: step.cumulative_tokens,
        });
    }
    out
}

// ===========================================================================
//  Evaluation: run every clock on a real trace and collect leads.
// ===========================================================================

struct ClockLeads {
    wall: usize,
    step: usize,
    token: usize,
    base_token: usize,
    base_belief: usize,
    agentic_full: usize,
    agentic_honest: usize,
    /// alarm steps (for context), event index.
    alarm_full: Option<usize>,
    alarm_honest: Option<usize>,
    alarm_base_belief: Option<usize>,
    event: usize,
    /// Why the honest agentic clock fired or didn't (alive-vs-degenerate check).
    diag_honest: ClockDiag,

    // --- M3b: ADAPTIVE (Page–Hinkley) detector, SAME detector on both sides ---
    /// Adaptive lead for agentic-honest (THE adaptive gate).
    adaptive_agentic_honest: usize,
    /// Adaptive lead for the fair belief-shift baseline.
    adaptive_base_belief: usize,
    /// Adaptive lead for the fair token-delta baseline.
    adaptive_base_token: usize,
    /// Adaptive lead for agentic-full (diagnostic; sees the error channel).
    adaptive_agentic_full: usize,
    /// Adaptive alarm steps (context).
    adaptive_alarm_honest: Option<usize>,
    adaptive_alarm_base_belief: Option<usize>,
    adaptive_alarm_base_token: Option<usize>,
}

/// Honest agentic weights: the standard weights, but contradiction = 0 so the
/// clock cannot read the very signal that defines the event.
fn honest_weights() -> AgenticWeights {
    AgenticWeights {
        contradiction: 0.0,
        ..AgenticWeights::default()
    }
}

fn evaluate_trace(states: &[AgentState], event: usize) -> ClockLeads {
    let bw = BASELINE_WINDOW;
    let k = K_SIGMA;

    let full = AgenticTime::new(AgenticWeights::default());
    let honest = AgenticTime::new(honest_weights());
    let base_token = WindowedDeltaClock::token_delta(bw);
    let base_belief = WindowedDeltaClock::belief_shift(bw);

    // M3b: the SAME adaptive detector, pre-registered, applied to every clock.
    let ph = adaptive_detector();

    ClockLeads {
        wall: early_warning_lead(&AgentWallClock, states, event, bw, k),
        step: early_warning_lead(&StepCountClock, states, event, bw, k),
        token: early_warning_lead(&TokenCountClock, states, event, bw, k),
        base_token: early_warning_lead(&base_token, states, event, bw, k),
        base_belief: early_warning_lead(&base_belief, states, event, bw, k),
        agentic_full: early_warning_lead(&full, states, event, bw, k),
        agentic_honest: early_warning_lead(&honest, states, event, bw, k),
        alarm_full: alarm_step(&full, states, bw, k),
        alarm_honest: alarm_step(&honest, states, bw, k),
        alarm_base_belief: alarm_step(&base_belief, states, bw, k),
        event,
        diag_honest: diagnose(&honest, states, event, bw, k),

        // Adaptive (Page–Hinkley) leads — identical detector on both sides.
        adaptive_agentic_honest: adaptive_early_warning_lead(&honest, states, event, &ph),
        adaptive_base_belief: adaptive_early_warning_lead(&base_belief, states, event, &ph),
        adaptive_base_token: adaptive_early_warning_lead(&base_token, states, event, &ph),
        adaptive_agentic_full: adaptive_early_warning_lead(&full, states, event, &ph),
        adaptive_alarm_honest: adaptive_alarm_step(&honest, states, &ph),
        adaptive_alarm_base_belief: adaptive_alarm_step(&base_belief, states, &ph),
        adaptive_alarm_base_token: adaptive_alarm_step(&base_token, states, &ph),
    }
}

// ===========================================================================
//  Candidate data locations (degrade gracefully if absent).
// ===========================================================================

fn candidate_dirs() -> Vec<PathBuf> {
    let mut dirs = Vec::new();
    if let Some(home) = std::env::var_os("USERPROFILE").or_else(|| std::env::var_os("HOME")) {
        let home = PathBuf::from(home);
        dirs.push(
            home.join(".claude")
                .join("projects")
                .join("C--Users-ruv-ruvector"),
        );
    }
    // Allow an explicit override for CI / other machines.
    if let Some(custom) = std::env::var_os("EMERGENT_TIME_TRACE_DIR") {
        dirs.push(PathBuf::from(custom));
    }
    dirs
}

/// Outcome of scanning the candidate directories: the parsed traces plus how
/// many `.jsonl` files were *seen* but produced no usable trace. The latter lets
/// `main` distinguish "no transcript files at all" from "files present but the
/// parser could not turn them into a scoreable trace" — an honesty distinction
/// (a silent parse failure must not masquerade as absence).
struct LoadOutcome {
    traces: Vec<RealTrace>,
    files_seen: usize,
    parse_failures: usize,
}

fn load_traces() -> LoadOutcome {
    for dir in candidate_dirs() {
        let Ok(entries) = fs::read_dir(&dir) else {
            continue;
        };
        let mut paths: Vec<PathBuf> = entries
            .flatten()
            .map(|e| e.path())
            .filter(|p| p.extension().and_then(|x| x.to_str()) == Some("jsonl"))
            .collect();
        paths.sort();
        if paths.is_empty() {
            continue;
        }
        // First directory that actually contains transcript files wins.
        let files_seen = paths.len();
        let mut traces = Vec::new();
        let mut parse_failures = 0;
        for p in paths {
            match parse_session(&p) {
                Some(t) => traces.push(t),
                None => parse_failures += 1,
            }
        }
        return LoadOutcome {
            traces,
            files_seen,
            parse_failures,
        };
    }
    LoadOutcome {
        traces: Vec::new(),
        files_seen: 0,
        parse_failures: 0,
    }
}

fn main() {
    println!("emergent-time : M3 real-trace defensibility gate");
    println!("================================================\n");

    print_preregistration();

    let LoadOutcome {
        traces,
        files_seen,
        parse_failures,
    } = load_traces();
    if traces.is_empty() {
        if files_seen > 0 {
            // Files were present but none parsed — surface it as a parse failure,
            // NOT as absence. Masquerading a parser gap as "no data" would hide a
            // real defect (and inflate confidence in the gate).
            println!(
                "\n[skip] Found {files_seen} transcript file(s) but parsed 0 usable trace(s) \
                 ({parse_failures} parse failure(s))."
            );
            println!("       The files exist but did not yield enough tool-issuing steps");
            println!("       (schema mismatch, or a very short session). This is a PARSE");
            println!("       gap, not missing data — fix the adapter rather than trusting");
            println!("       a silent skip.");
        } else {
            println!("\n[skip] No real session transcripts found.");
            println!("       Looked in: ~/.claude/projects/C--Users-ruv-ruvector/*.jsonl");
            println!("       (or $EMERGENT_TIME_TRACE_DIR). This is expected on CI / a fresh");
            println!("       checkout without recorded sessions — the harness degrades");
            println!("       gracefully and exits 0 rather than panicking.");
        }
        println!("\n       The protocol, adapter, and pre-registered thresholds above are");
        println!("       the reusable deliverable; supply traces to produce numbers.");
        return;
    }

    if parse_failures > 0 {
        println!(
            "\n[note] {parse_failures} of {files_seen} transcript file(s) did not parse \
             (reported, not hidden)."
        );
    }
    println!("\nLoaded {} real session transcript(s).\n", traces.len());

    let mut all: Vec<ClockLeads> = Vec::new();
    let mut descriptive_only = 0usize;

    for tr in &traces {
        let states = to_agent_states(&tr.steps);
        println!("--- trace {} ---", tr.name);
        println!(
            "  steps: {:>4}   total is_error results: {:>3}",
            states.len(),
            tr.total_errors
        );
        match tr.event_index {
            None => {
                descriptive_only += 1;
                println!(
                    "  event-to-predict: NONE (no error cascade of ≥{} errors within {} steps)",
                    CASCADE_MIN_ERRORS, CASCADE_SPAN
                );
                println!("  -> descriptive only: cannot score a lead on this trace.\n");
            }
            Some(ev) => {
                if ev < MIN_STEPS {
                    println!(
                        "  event-to-predict: step {ev} (too early — inside the baseline \
                         window; no room to lead). Descriptive only.\n"
                    );
                    descriptive_only += 1;
                    continue;
                }
                let l = evaluate_trace(&states, ev);
                print_trace_leads(&l);
                all.push(l);
            }
        }
    }

    print_summary(&all, descriptive_only, traces.len());
}

